from itertools import product

def f():
    """
    Подсчитывает количество пятибуквенных слов из алфавита ['A','B','C','D','E'],
    в которых НЕ выполняется одновременно: первая буква 'E' И последняя буква 'A'.

    Всего комбинаций: 5^5 = 3125
    Запрещённых (первая='E', последняя='A'): 5^3 = 125
    Итого: 3125 - 125 = 3000

    >>> f()
    3000

    >>> # Проверка: результат меньше полного числа комбинаций 5^5
    >>> f() < 5**5
    True

    >>> # Проверка: результат больше нуля
    >>> f() > 0
    True
    """
    alf = ['A', 'B', 'C', 'D', 'E']
    s = [
        ''.join(i)
        for i in product(alf, repeat=5)
        if not (i[0] == 'E' and i[4] == 'A')  # первая не 'E' или последняя не 'A'
    ]
    return len(s)


if __name__ == '__main__':
    import doctest
    doctest.testmod(verbose=True)
    print(f())
