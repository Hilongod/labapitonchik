def f():
    """
    Подсчитывает количество единиц в двоичном представлении числа
    4^511 + 2^511 - 511.

    Разбор по битам:
      4^511 = 2^1022  → единица в позиции 1022
      2^511 - 511     → единицы в позициях 9..510 (502 бита) и позиция 0
      Итого единиц: 1 + 502 + 1 = 504

    >>> f()
    504

    >>> # Результат — целое положительное число
    >>> f() > 0
    True

    >>> # Быстрая проверка на малом примере: единицы в bin(4^2 + 2^2 - 3)
    >>> # 16 + 4 - 3 = 17 = 0b10001 → 2 единицы
    >>> bin(4**2 + 2**2 - 3).count('1')
    2
    """
    a = 4**511 + 2**511 - 511
    return bin(a).count('1')


