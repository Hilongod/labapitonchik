def f(n):
    """
    Возвращает список первых пяти делителей числа n (начиная с 2),
    или меньше, если делителей не хватает.

    >>> f(30)
    [2, 3, 5, 6, 10]

    >>> # У простого числа нет делителей в диапазоне (2, n)
    >>> f(7)
    []

    >>> # n=12: делители 2,3,4,6 — меньше пяти, возвращаем все что есть
    >>> f(12)
    [2, 3, 4, 6]

    >>> # n=1: range(2,1) пуст — делителей нет
    >>> f(1)
    []

    >>> # Длина результата не превышает 5
    >>> len(f(360)) <= 5
    True
    """
    s = []
    for i in range(2, n):
        if n % i == 0:
            s.append(i)
        if len(s) == 5:
            break
    return s


def f1():
    """
    Находит 5 чисел N >= 200_000_001, у которых ровно 5 делителей
    (среди делителей от 2 до N-1) и произведение этих делителей M(N)
    удовлетворяет условию 0 < M(N) < N.

    Возвращает список кортежей (N, M(N)).

    >>> result = f1()

    >>> # Ровно 5 результатов
    >>> len(result)
    5

    >>> # Каждый элемент — кортеж из двух чисел
    >>> all(isinstance(n, int) and isinstance(m, int) for n, m in result)
    True

    >>> # Для каждого результата выполняется условие 0 < M < N
    >>> all(0 < m < n for n, m in result)
    True

    >>> # Первый найденный результат
    >>> result[0]
    (200000004, 1728)
    """
    res = []
    n = 200_000_001
    while len(res) < 5:
        s = f(n)
        if len(s) == 5:
            m = s[0] * s[1] * s[2] * s[3] * s[4]
            if 0 < m < n:
                res.append((n, m))
        n += 1
    return res


if __name__ == '__main__':
    import doctest
    doctest.testmod(verbose=True)
    for n, m in f1():
        print(f"N={n}, M(N)={m}")
